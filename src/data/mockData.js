  // Helper function to get statistics by country
  export const getTestimonialStats = () => {
    const stats = {};
    testimonials.forEach(t => {
      if (!stats[t.country]) {
        stats[t.country] = { count: 0, totalRating: 0, verified: 0 };
      }
      stats[t.country].count++;
      stats[t.country].totalRating += t.rating;
      if (t.verified) stats[t.country].verified++;
    });
    
    Object.keys(stats).forEach(country => {
      stats[country].avgRating = (stats[country].totalRating / stats[country].count).toFixed(1);
    });
    
    return stats;
  };
  
